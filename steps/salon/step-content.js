function currentDay() {
  const explicitDay = document.body.dataset.day;
  if (explicitDay) return Number(explicitDay);
  const fileName = location.pathname.split('/').pop() || '';
  const dayText = fileName.replace('day', '').replace('.html', '');
  return Number(dayText) || 1;
}

function escapeHtml(value) {
  return String(value || '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll('\'', '&#39;');
}

function formatText(value) {
  const normalized = String(value || '')
    .replaceAll('〇〇さん、こんにちは。', 'こんにちは。')
    .replaceAll('[https://hide-ai-lab.com/salon_sindan_si-to.html](https://hide-ai-lab.com/salon_sindan_si-to.html)', '<a href=https://hide-ai-lab.com/salon_sindan_si-to.html target=_blank rel=noopener>https://hide-ai-lab.com/salon_sindan_si-to.html</a>')
    .replaceAll('[https://hide-ai-lab.com/salon_soudan_yoyaku.html](https://hide-ai-lab.com/salon_soudan_yoyaku.html)', '<a href=https://hide-ai-lab.com/salon_soudan_yoyaku.html target=_blank rel=noopener>https://hide-ai-lab.com/salon_soudan_yoyaku.html</a>');

  return normalized
    .split(String.fromCharCode(10))
    .map((line) => {
      if (line.includes('<a href=')) return line;
      return escapeHtml(line);
    })
    .join('<br>');
}

function renderPage() {
  const day = currentDay();
  const page = STEP_PAGES[day] || STEP_PAGES[1];
  document.title = page.title + ' | ヒデ サロンAIステップ配信';
  document.getElementById('day-label').textContent = 'Day ' + day;
  document.getElementById('title').textContent = page.title;
  document.getElementById('meta-day').textContent = '登録後 ' + day + ' 日目';
  document.getElementById('meta-theme').textContent = page.flexTitle;

  const body = document.getElementById('article-body');
  body.innerHTML = page.body.map((paragraph) => {
    const className = paragraph.length <= 18 && !paragraph.includes(String.fromCharCode(10)) ? 'short' : '';
    return `<p class='${className}'>${formatText(paragraph)}</p>`;
  }).join('');

  const visual = document.getElementById('visual');
  if (page.visual && page.visual.length) {
    visual.innerHTML = `<div class='visual-note'><h2>今日の整理ポイント</h2><div class='pill-grid'>${page.visual.map((item) => `<span>${escapeHtml(item)}</span>`).join('')}</div></div>`;
  }

  const cta = document.getElementById('cta');
  if (page.ctaUrl) {
    cta.innerHTML = `<div class='cta'><h2>${escapeHtml(page.ctaTitle)}</h2><p>${escapeHtml(page.ctaText)}</p><a class='button' href='${escapeHtml(page.ctaUrl)}'>作戦会議に申し込む</a></div>`;
  }
}

document.addEventListener('DOMContentLoaded', renderPage);
